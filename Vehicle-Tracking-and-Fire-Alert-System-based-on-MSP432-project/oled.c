/** sendCommand function sends command to the I2C communication **/
void sendCommand(unsigned char cmd)
{
    unsigned char i2c_data[2] = {SSD1306_CONTROL_REG,cmd};
    i2c_send(i2c_data,2);
}
/** sendData function sends data to the I2C communication **/
void sendData(unsigned char *data)
{
    int i;
    unsigned char i2c_data[2] = {0};
    i2c_data[0] = SSD1306_DATA_REG;
    for(i=0;i<1024;i++)
    {
        i2c_data[1] = data[i];
        i2c_send(i2c_data,2);
    }
}

/** write_display function writes data to the OLED display **/
void write_display(unsigned char *data)
{
    int i;
    unsigned char i2c_data[2];
    i2c_data[0] = SSD1306_DATA_REG;
    for(i=0;i<1024;i++)
    {
        i2c_data[1] = data[i];
        i2c_send(i2c_data,2);
    }
}

